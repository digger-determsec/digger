/// Blind Evaluation — evaluate without exposing expected findings to the engine.
///
/// The reasoning engine should not know expected findings during execution.
/// Evaluation compares generated findings against stored expectations only after execution.
use digger_benchmark::loader::findings_match;
use digger_benchmark::models::LoadedExploit;
use digger_graph::build_system_ir;
use digger_hypothesis::analyze_compat as analyze;
use digger_parser::parse_program;

/// Blind evaluation result for a single case.
#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]
pub struct BlindEvaluationResult {
    /// Exploit ID.
    pub exploit_id: String,
    /// Whether the engine detected the expected vulnerability.
    pub detected: bool,
    /// Findings generated by the engine.
    pub findings_generated: Vec<String>,
    /// Expected findings (revealed after execution).
    pub expected_findings: Vec<String>,
    /// Matched findings.
    pub matched: Vec<String>,
    /// Missed findings.
    pub missed: Vec<String>,
    /// False positives (unexpected findings).
    pub false_positives: Vec<String>,
    /// Precision.
    pub precision: f64,
    /// Recall.
    pub recall: f64,
    /// F1 score.
    pub f1: f64,
    /// Runtime (ms).
    pub runtime_ms: f64,
    /// Whether this case is from a known protocol (has semantic pack).
    pub is_known_protocol: bool,
    /// Whether this case is a generalization test.
    pub is_generalization: bool,
}

/// Blind evaluation report.
#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]
pub struct BlindEvaluationReport {
    /// All results.
    pub results: Vec<BlindEvaluationResult>,
    /// Aggregate precision.
    pub aggregate_precision: f64,
    /// Aggregate recall.
    pub aggregate_recall: f64,
    /// Aggregate F1.
    pub aggregate_f1: f64,
    /// Generalization results (unknown protocols).
    pub generalization: GeneralizationMetrics,
    /// Runtime summary.
    pub runtime_summary: RuntimeSummary,
}

/// Generalization metrics.
#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]
pub struct GeneralizationMetrics {
    /// Total generalization cases.
    pub total_generalization: usize,
    /// Generalization recall.
    pub generalization_recall: f64,
    /// Generalization precision.
    pub generalization_precision: f64,
    /// Known protocol recall.
    pub known_protocol_recall: f64,
    /// Recall gap (known - generalization).
    pub recall_gap: f64,
}

/// Runtime summary.
#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]
pub struct RuntimeSummary {
    /// Average runtime per case (ms).
    pub avg_runtime_ms: f64,
    /// Max runtime (ms).
    pub max_runtime_ms: f64,
    /// Min runtime (ms).
    pub min_runtime_ms: f64,
    /// Total runtime (ms).
    pub total_runtime_ms: f64,
}

/// Known protocols with semantic packs.
const KNOWN_PROTOCOLS: &[&str] = &[
    "erc4626",
    "uniswap",
    "aave",
    "compound",
    "morpho",
    "lido",
    "eigenlayer",
    "safe",
    "erc4337",
    "maker",
];

/// Run blind evaluation on a corpus.
///
/// The engine runs without knowing expected findings.
/// Evaluation happens only after execution completes.
pub fn run_blind_evaluation(corpus: &[LoadedExploit]) -> BlindEvaluationReport {
    let mut results = Vec::new();

    for exploit in corpus {
        let result = evaluate_blind(exploit);
        results.push(result);
    }

    // Aggregate
    let total_tp: usize = results.iter().map(|r| r.matched.len()).sum();
    let total_fp: usize = results.iter().map(|r| r.false_positives.len()).sum();
    let total_fn: usize = results.iter().map(|r| r.missed.len()).sum();

    let aggregate_precision = if total_tp + total_fp > 0 {
        total_tp as f64 / (total_tp + total_fp) as f64
    } else {
        0.0
    };
    let aggregate_recall = if total_tp + total_fn > 0 {
        total_tp as f64 / (total_tp + total_fn) as f64
    } else {
        0.0
    };
    let aggregate_f1 = if aggregate_precision + aggregate_recall > 0.0 {
        2.0 * aggregate_precision * aggregate_recall / (aggregate_precision + aggregate_recall)
    } else {
        0.0
    };

    // Generalization metrics
    let generalization_results: Vec<&BlindEvaluationResult> =
        results.iter().filter(|r| r.is_generalization).collect();
    let known_results: Vec<&BlindEvaluationResult> =
        results.iter().filter(|r| !r.is_generalization).collect();

    let gen_tp: usize = generalization_results.iter().map(|r| r.matched.len()).sum();
    let gen_fn: usize = generalization_results.iter().map(|r| r.missed.len()).sum();
    let gen_fp: usize = generalization_results
        .iter()
        .map(|r| r.false_positives.len())
        .sum();

    let known_tp: usize = known_results.iter().map(|r| r.matched.len()).sum();
    let known_fn: usize = known_results.iter().map(|r| r.missed.len()).sum();
    let _known_fp: usize = known_results.iter().map(|r| r.false_positives.len()).sum();

    let generalization_recall = if gen_tp + gen_fn > 0 {
        gen_tp as f64 / (gen_tp + gen_fn) as f64
    } else {
        0.0
    };
    let generalization_precision = if gen_tp + gen_fp > 0 {
        gen_tp as f64 / (gen_tp + gen_fp) as f64
    } else {
        0.0
    };
    let known_protocol_recall = if known_tp + known_fn > 0 {
        known_tp as f64 / (known_tp + known_fn) as f64
    } else {
        0.0
    };

    let generalization = GeneralizationMetrics {
        total_generalization: generalization_results.len(),
        generalization_recall,
        generalization_precision,
        known_protocol_recall,
        recall_gap: known_protocol_recall - generalization_recall,
    };

    // Runtime summary
    let runtimes: Vec<f64> = results.iter().map(|r| r.runtime_ms).collect();
    let runtime_summary = RuntimeSummary {
        avg_runtime_ms: if runtimes.is_empty() {
            0.0
        } else {
            runtimes.iter().sum::<f64>() / runtimes.len() as f64
        },
        max_runtime_ms: runtimes.iter().cloned().fold(f64::NEG_INFINITY, f64::max),
        min_runtime_ms: runtimes.iter().cloned().fold(f64::INFINITY, f64::min),
        total_runtime_ms: runtimes.iter().sum(),
    };

    BlindEvaluationReport {
        results,
        aggregate_precision,
        aggregate_recall,
        aggregate_f1,
        generalization,
        runtime_summary,
    }
}

/// Evaluate a single exploit in blind mode.
fn evaluate_blind(exploit: &LoadedExploit) -> BlindEvaluationResult {
    let start = std::time::Instant::now();

    // Run engine WITHOUT exposing expected findings
    let raw = parse_program(&exploit.source_code, &exploit.language);
    let ir = build_system_ir(raw);
    let findings = analyze(&ir);

    let runtime_ms = start.elapsed().as_secs_f64() * 1000.0;

    let findings_generated: Vec<String> = findings.iter().map(|f| f.kind.clone()).collect();
    let expected = &exploit.meta.expected_findings;

    // Compare AFTER execution
    let matched: Vec<String> = expected
        .iter()
        .filter(|e| findings_generated.iter().any(|d| findings_match(d, e)))
        .cloned()
        .collect();

    let missed: Vec<String> = expected
        .iter()
        .filter(|e| !findings_generated.iter().any(|d| findings_match(d, e)))
        .cloned()
        .collect();

    let false_positives: Vec<String> = findings_generated
        .iter()
        .filter(|d| !expected.iter().any(|e| findings_match(d, e)))
        .cloned()
        .collect();

    let tp = matched.len();
    let fp = false_positives.len();
    let fn_ = missed.len();

    let precision = if tp + fp > 0 {
        tp as f64 / (tp + fp) as f64
    } else {
        0.0
    };
    let recall = if tp + fn_ > 0 {
        tp as f64 / (tp + fn_) as f64
    } else {
        0.0
    };
    let f1 = if precision + recall > 0.0 {
        2.0 * precision * recall / (precision + recall)
    } else {
        0.0
    };

    let protocol_lower = exploit.meta.protocol.to_lowercase();
    let is_known = KNOWN_PROTOCOLS.iter().any(|p| protocol_lower.contains(p));

    BlindEvaluationResult {
        exploit_id: exploit.meta.exploit_id.clone(),
        detected: missed.is_empty(),
        findings_generated,
        expected_findings: expected.clone(),
        matched,
        missed,
        false_positives,
        precision,
        recall,
        f1,
        runtime_ms,
        is_known_protocol: is_known,
        is_generalization: !is_known,
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use digger_benchmark::models::ExploitMeta;

    fn make_exploit(protocol: &str, findings: Vec<&str>) -> LoadedExploit {
        LoadedExploit {
            meta: ExploitMeta {
                exploit_id: "test-001".into(),
                vulnerability_class: "reentrancy".into(),
                protocol: protocol.into(),
                chain: "ethereum".into(),
                year: 2024,
                loss_usd: 0,
                expected_findings: findings.into_iter().map(String::from).collect(),
                expected_path_types: vec![],
                expected_hypotheses: vec![],
                known_limitations: vec![],
            },
            source_code: r#"
contract Test {
    function withdraw() public {
        (bool success,) = msg.sender.call{value: address(this).balance}("");
        balances[msg.sender] = 0;
    }
}
"#
            .into(),
            language: "solidity".into(),
            source_path: "test.sol".into(),
        }
    }

    #[test]
    fn blind_evaluation_produces_results() {
        let exploits = vec![make_exploit("TestProtocol", vec!["ReentrancyRisk"])];
        let report = run_blind_evaluation(&exploits);
        assert_eq!(report.results.len(), 1);
        assert!(!report.results[0].findings_generated.is_empty());
    }

    #[test]
    fn generalization_detection() {
        let known = make_exploit("Uniswap", vec!["ReentrancyRisk"]);
        let unknown = make_exploit("UnknownProtocol", vec!["ReentrancyRisk"]);
        let report = run_blind_evaluation(&[known, unknown]);

        assert_eq!(report.generalization.total_generalization, 1);
        assert!(report.results.iter().any(|r| r.is_generalization));
        assert!(report.results.iter().any(|r| !r.is_generalization));
    }
}
